import turtle

aken = turtle.Screen()
aken.bgcolor("PaleTurquoise")

turtle.speed(2)
turtle.showturtle()
turtle.shape("turtle")
turtle.pensize(5)




for i in range(5):
    turtle.goto(0, -0)
    turtle.left(72)
    turtle.forward(-50)
    turtle.pencolor("OrangeRed")

    for i in range(4):
        turtle.forward(100)
        turtle.right(90)
        turtle.pencolor("DeepPink")
        
        

turtle.penup()
turtle.goto(-300, 100)
        


turtle.done()
    










